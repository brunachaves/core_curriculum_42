#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str_result;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	str_result = (char *)malloc(len * (sizeof(char)) + 1);
	if (!str_result)
		return (NULL);
	ft_strlcpy(str_result, (s + start), (len + 1));
	return (str_result);
}
