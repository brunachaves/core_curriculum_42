#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*aux;

	aux = lst;
	while (lst)
	{
		aux = lst -> next;
		(*f)(lst -> content);
		lst = aux;
	}
}
